const { assert } = require('chai');
const request = require('./request');
const { dropCollection } = require('./db');


describe('Authorization API', () => {

    beforeEach(() => 
        dropCollection('reviewers'));

    let token = null;

    beforeEach(() => {
        return request.post('api/auth/signup')
            .send({
                email:'myname@me.com',
                password: 'qwerty'
            })
            .then(({ body }) => token = body.token);

    });
    
    it('signup', () => {
        assert.ok(token);
    });

});